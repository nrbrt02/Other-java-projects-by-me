    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        if (names.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Names can not be Empty");
        } else if (!isValidEmail(email.getText())) {
            JOptionPane.showMessageDialog(this, "Email not Valid");
        } else if (!phone.getText().matches("^07\\d{8}$")) {
            JOptionPane.showMessageDialog(this, "Phone number not Valid");
        } else if (jComboBox1.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this, "Select Faculty");
        } else if (gender.getSelection() == null) {
            JOptionPane.showMessageDialog(this, "Select Gender");
        } else if (!startingYear.getText().matches("^\\d{4}$")) {
            JOptionPane.showMessageDialog(this, "Year formata YYYY");
        } else {
            try {
                Student stud = new Student();

                if (allStudentsTable.getSelectedRow() != -1) {
                    int selectedRow = allStudentsTable.getSelectedRow();
                    DefaultTableModel tbModel = (DefaultTableModel) allStudentsTable.getModel();
                    stud.setStudentId(Integer.parseInt(tbModel.getValueAt(selectedRow, 0).toString()));
                }
                stud.setNames(names.getText());
                stud.setEmail(email.getText());
                stud.setPhone(phone.getText());
                stud.setFaculty(jComboBox1.getSelectedItem().toString());
                stud.setGender(gender.getSelection().toString());
                if (male.isSelected()) {
                    stud.setGender(male.getText());
                } else {
                    stud.setGender(female.getText());
                }
                stud.setStartingYear(startingYear.getText());
                
                Registry theReg = LocateRegistry.getRegistry("127.0.0.1", 81);
                StudentServices service = (StudentServices) theReg.lookup("student");
                Student StudentObj = service.saveStudent(stud);

                if (StudentObj != null) {
                    JOptionPane.showMessageDialog(this, "Data saved");
                    names.setText("");
                    email.setText("");
                    phone.setText("");
                    jComboBox1.setSelectedIndex(0);
                    gender.clearSelection();
                    startingYear.setText("");
                    allRetrived();
                } else {
                    JOptionPane.showMessageDialog(this, "Something wrong");
                }
            } catch (Exception e) {
            }
        }
    }//GEN-LAST:event_saveActionPerformed

    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        if (allStudentsTable.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, "Select a row To delete");
        } else {
            int choice = JOptionPane.showConfirmDialog(this, "Are you sure to delete");
            if (choice == JOptionPane.YES_OPTION) {
                int selectedRow = allStudentsTable.getSelectedRow();
                DefaultTableModel tbModel = (DefaultTableModel) allStudentsTable.getModel();
                Student stud = new Student();
                stud.setStudentId(Integer.parseInt(tbModel.getValueAt(selectedRow, 0).toString()));
                try {
                    Registry theReg = LocateRegistry.getRegistry("127.0.0.1", 81);
                    StudentServices service = (StudentServices) theReg.lookup("student");
                    Student StudentObj = service.delete(stud);

                    if (StudentObj != null) {
                        JOptionPane.showMessageDialog(this, "Data saved");
                        names.setText("");
                        email.setText("");
                        phone.setText("");
                        jComboBox1.setSelectedIndex(0);
                        gender.clearSelection();
                        startingYear.setText("");
                        allRetrived();
                    } else {
                        JOptionPane.showMessageDialog(this, "Something wrong");
                    }
                } catch (Exception e) {
                    e.printStackTrace();
                }

            }
        }
    }//GEN-LAST:event_deleteActionPerformed

    private void allStudentsTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_allStudentsTableMouseClicked
        int selectedRow = allStudentsTable.getSelectedRow();
        DefaultTableModel tbModel = (DefaultTableModel) allStudentsTable.getModel();

        names.setText(tbModel.getValueAt(selectedRow, 1).toString());
        email.setText(tbModel.getValueAt(selectedRow, 2).toString());
        phone.setText(tbModel.getValueAt(selectedRow, 3).toString());
        jComboBox1.setSelectedItem(tbModel.getValueAt(selectedRow, 4).toString());
        if (tbModel.getValueAt(selectedRow, 4).toString().equals("Male")) {
            male.setSelected(true);
        } else {
            female.setSelected(true);
        }
        startingYear.setText(tbModel.getValueAt(selectedRow, 6).toString());
    }//GEN-LAST:event_allStudentsTableMouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (searchTxt.getText().trim().isEmpty() || !searchTxt.getText().matches("^[0-9]*$")) {
            JOptionPane.showMessageDialog(this, "Search not valid\nOnly Search Ids");
        } else {
            Student stud = new Student();
            int theId = Integer.parseInt(searchTxt.getText());
            stud.setStudentId(theId);
            try {
                Registry theReg = LocateRegistry.getRegistry("127.0.0.1", 81);
                StudentServices service = (StudentServices) theReg.lookup("student");
                Student StudentObj = service.searchStudent(stud);

                if (StudentObj != null) {
                    DefaultTableModel tbModel = (DefaultTableModel) allStudentsTable.getModel();
                    tbModel.setRowCount(0);

                    Integer id = StudentObj.getStudentId();
                    String names = StudentObj.getNames();
                    String email = StudentObj.getEmail();
                    String phone = StudentObj.getPhone();
                    String faculty = StudentObj.getFaculty();
                    String gender = StudentObj.getGender();
                    String startingYear = StudentObj.getStartingYear();
                    tbModel.addRow(new Object[]{
                        id, names, email, phone, faculty, gender, startingYear});
                } else {
                    JOptionPane.showMessageDialog(this, "No Results");
                }
            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

