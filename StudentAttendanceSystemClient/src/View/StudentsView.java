    void allRetrived() {
        DefaultTableModel tbModel = (DefaultTableModel) allStudentsTable.getModel();
        try {
            Registry theReg = LocateRegistry.getRegistry("127.0.0.1", 81);
            StudentServices service = (StudentServices) theReg.lookup("student");
            List<Student> allStudents = service.allStudents();
            tbModel.setRowCount(0);
            for (Student student : allStudents) {
                Integer id = student.getStudentId();
                String names = student.getNames();
                String email = student.getEmail();
                String phone = student.getPhone();
                String faculty = student.getFaculty();
                String gender = student.getGender();
                String startingYear = student.getStartingYear();
                tbModel.addRow(new Object[]{
                    id, names, email, phone, faculty, gender, startingYear});
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
